<?php

namespace app\controllers;

use Yii;
use yii\filters\AccessControl;
use yii\web\Controller;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;

class SiteController extends Controller
{
    const EVENT_REGISTRATION = '1';
    const EVENT_VERIFICATION = '2';
    const EVENT_LOGIN = '3';
    const EVENT_SENDMESSAGE = '4';
    const EVENT_LOGOUT = '5';

    /**
     * {@inheritdoc}
     */

    public function beforeAction($action){

        $this->on(self::EVENT_LOGIN, ['app\helpers\subscriptionshelper', 'subscriptionHandler']);
        $this->on(self::EVENT_LOGOUT, ['app\helpers\subscriptionshelper', 'subscriptionHandler']);
        $this->on(self::EVENT_REGISTRATION, ['app\helpers\subscriptionshelper', 'subscriptionHandler']);
        $this->on(self::EVENT_VERIFICATION, ['app\helpers\subscriptionshelper', 'subscriptionHandler']);
        $this->on(self::EVENT_SENDMESSAGE, ['app\helpers\subscriptionshelper', 'subscriptionHandler']);

        return parent::beforeAction($action);
    }

    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::class,
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::class,
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        return $this->render('index');
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }
        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            $this->trigger(self::EVENT_LOGIN);
            return $this->goBack();
        }
        $model->password = '';

        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        $this->trigger(self::EVENT_LOGOUT);
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }

    /**
     * Displays about page.
     *
     * @return string
     */
    public function actionAbout()
    {
        return $this->render('about');
    }
}
